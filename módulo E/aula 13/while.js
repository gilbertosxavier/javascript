/* 
No comando while é feito um teste no início e enquanto a condição
for satisfeita um bloco de códigos é executado
Essa estrutura é chamada de:
"Estrutura de repetição com teste lógico no início "
*/

let c = 1 // variável 'c' é um contador 

while (c <= 6) {
    console.log(`Passo ${c}`) //aqui uma mensagem é impressa no console
    c++ //aqui é incrementado o valor de 'c'.
}